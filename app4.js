//! Fonksiyonlar

//? Paramtresiz Fonksiyon
function topla() {
    console.log(5+9);
}
topla();

//? Parametreli Fonksiyon
function topla2(a, b) {
    console.log(a+b);
}
topla2(213, 9);

//? Geriye Değer Döndüren Fonksiyon

function topla3(a, b) {
    return a+b;
}
console.log(topla3(5, 9));

//? Return bir değeri fonksiyonun dışına çıkartır.
//? Return'den sonra bir işlem yazılmaz.

//! String Metotları

//? charAt() metodu belirtilen index numarasındaki karakteri döndürür.
//? concat() metodu bir veya daha fazla stringi birleştirir.
//? endsWith() metodu bir stringin belirtilen karakterle bitip bitmediğini kontrol eder.
//? includes() metodu bir stringin belirtilen karakterleri içerip içermediğini kontrol eder.
//? indexOf() metodu bir stringin belirtilen karakterin ilk indeksini döndürür.
//? lastIndexOf() metodu bir stringin belirtilen karakterin son indeksini döndürür.
//? match() metodu bir stringin belirtilen karakterlerle eşleşip eşleşmediğini kontrol eder.
//? repeat() metodu bir stringi belirtilen sayıda tekrarlar.
//? replace() metodu bir stringin belirtilen karakterlerini değiştirir.
//? search() metodu bir stringin belirtilen karakterlerini arar ve ilk indeksi döndürür.
//? slice() metodu bir stringin belirtilen indeksler arasındaki karakterleri döndürür.
//? split() metodu bir stringi belirtilen karakterden böler ve bir diziye dönüştürür.
//? startsWith() metodu bir stringin belirtilen karakterle başlayıp başlamadığını kontrol eder.
//? substr() metodu bir stringin belirtilen indeksten sonraki karakterleri döndürür.
//? substring() metodu bir stringin belirtilen indeksler arasındaki karakterleri döndürür.
//? toLowerCase() metodu bir stringi küçük harfe dönüştürür.
//? toUpperCase() metodu bir stringi büyük harfe dönüştürür.
//? trim() metodu bir stringin başındaki ve sonundaki boşlukları kaldırır.
//? valueOf() metodu bir stringin değerini döndürür.

//! Math Metotları

//? Math.PI - Pi sayısını döndürür.
//? Math.abs() - Bir sayının mutlak değerini döndürür.
//? Math.ceil() - Bir sayıyı yukarı yuvarlar.
//? Math.floor() - Bir sayıyı aşağı yuvarlar.
//? Math.round() - Bir sayıyı en yakın sayıya yuvarlar.
//? Math.max() - Bir dizideki en büyük sayıyı döndürür.
//? Math.min() - Bir dizideki en küçük sayıyı döndürür.
//? Math.pow() - Bir sayının üssünü alır.
//? Math.sqrt() - Bir sayının karekökünü alır.
//? Math.random() - 0 ile 1 arasında rastgele bir sayı döndürür.

